defined('ABSPATH') || exit;

global $product;

/**
 * Hook: woocommerce_before_single_product.
 *
 * @hooked woocommerce_output_all_notices - 10
 */
do_action('woocommerce_before_single_product');

if (post_password_required()) {
	echo get_the_password_form(); // WPCS: XSS ok.
	return;
}
?>

<div id="product-<?php the_ID(); ?>" <?php wc_product_class('', $product); ?>>


	<?php
	/**
	 * Hook: woocommerce_before_single_product_summary.
	 *
	 * @hooked woocommerce_show_product_sale_flash - 10
	 * @hooked woocommerce_show_product_images - 20
	 */
	do_action('woocommerce_before_single_product_summary');
	?>

	<div class="summary entry-summary">
		<?php
		/**
		 * Hook: woocommerce_single_product_summary.
		 *
		 * @hooked woocommerce_template_single_title - 5
		 * @hooked woocommerce_template_single_rating - 10
		 * @hooked woocommerce_template_single_price - 10
		 * @hooked woocommerce_template_single_excerpt - 20
		 * @hooked woocommerce_template_single_add_to_cart - 30
		 * @hooked woocommerce_template_single_meta - 40
		 * @hooked woocommerce_template_single_sharing - 50
		 * @hooked WC_Structured_Data::generate_product_data() - 60
		 */
		do_action('woocommerce_single_product_summary');
		?>
		<div class="wiev-product">
			Просмотров:
			<?php
			// Получаем данные о просмотре с базы для продукта
			$count = get_post_meta($post->ID, 'views', true);
			if ($count > 0) {
				echo $count;
			} else {
				echo "0";
			}
			?>
		</div>
		<div class="product-order">
		<?php
			// получаем все заказы по текущему товару
			$orders_ids = get_orders_ids_by_product_id($product->get_id());
			// проверяем есть ли заказы
			if (count($orders_ids)) {
				// получаем последний заказ
				$order_id = end($orders_ids);
				// получаем заказ для получения даты
				$order = wc_get_order($order_id);
				// получаем дату заказа
				$date = $order->get_date_created();
				// форматируем дату
				echo "Последний заказ: " . $date->format('d.m.Y');
			}
			?>

		</div>
	</div>

	<?php
	/**
	 * Hook: woocommerce_after_single_product_summary.
	 *
	 * @hooked woocommerce_output_product_data_tabs - 10
	 * @hooked woocommerce_upsell_display - 15
	 * @hooked woocommerce_output_related_products - 20
	 */
	do_action('woocommerce_after_single_product_summary');
	?>
</div>

<?php do_action('woocommerce_after_single_product'); ?>